    private void incribirseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_incribirseMouseClicked
        consultar.inscribirse(metodo.ncontrol,clave_grupo);
        consultar.inscrito=true;
        
        String userHome = System.getProperty("user.home");
        String array[] = new String[6];
        String[] actual =new String[4];
        actual=consultar.actividadActual2( login.ncontrol);
        array=consultar.seleccionarAlumno(login.ncontrol);
        documento.generar_horario(userHome+"/Downloads/Horario-"+array[0]+" "+array[1]+" "+array[2] +".pdf",login.ncontrol,array[0],array[1],array[2],array[3],Integer.parseInt(array[4]),Integer.parseInt(actual[2]),actual[0],actual[4],actual[3],actual[1]);
        correo.createEmail(array[5],userHome+"/Downloads/Horario-"+array[0]+" "+array[1]+" "+array[2] +".pdf");
        correo.sendEmail();
        
        menuAlumno ventana  = new menuAlumno();   
        ventana.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_incribirseMouseClicked

