    public void cargarDatosAlumnos(String claveGrupo) {
        consultas consultas = new consultas();
        List<String[]> studentDetails = consultas.detallesEstudiantesXgrupo(claveGrupo);

        DefaultTableModel model = (DefaultTableModel) tablaEvalua.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de cargar los datos

        for (String[] details : studentDetails) {
            model.addRow(new Object[]{details[0], details[1], ""}); // Número de control, nombre, estatus vacío
        }
    }


   

