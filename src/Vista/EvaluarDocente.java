    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        int result = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea guardar los cambios? Esta acción es irreversible.", "Confirmación", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) tablaEvalua.getModel();
            consultas consultas = new consultas();

            boolean allChecked = true;
            for (int i = 0; i < model.getRowCount(); i++) {
                String estatus = (String) model.getValueAt(i, 2);

                if (estatus == null || estatus.isEmpty()) {
                    allChecked = false;
                    break;
                }
            }

            if (!allChecked) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una opción (Aprobado o No aprobado) para todos los alumnos.");
                return;
            }

            for (int i = 0; i < model.getRowCount(); i++) {
                String numeroControl = (String) model.getValueAt(i, 0);
                String estatus = (String) model.getValueAt(i, 2);
                String estado = estatus.equals("APROBADO") ? "APROBADO" : "REPROBADO";

                consultas.actualizarEstatusAlumno(numeroControl, claveGrupo, estado);
            }

            JOptionPane.showMessageDialog(this, "Cambios guardados exitosamente.");
            this.dispose();
        }

    }//GEN-LAST:event_btnGuardarMouseClicked

